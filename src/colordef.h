/* Foreground colors */

#define RED			"{R"
#define BLUE		"{B"
#define GREEN		"{G"
#define BLACK		"{D"
#define WHITE		"{W"
#define MAGENTA		"{M"
#define YELLOW		"{Y"
#define CYAN		"{C"

/* Background colors */

#define RED_BG			"{1"
#define BLUE_BG			"{3"
#define GREEN_BG		"{2"
#define BLACK_BG		"{0"
#define WHITE_BG		"{7"
#define MAGENTA_BG		"{5"
#define YELLOW_BG		"{6"
#define CYAN_BG			"{4"

/* Other codes */

#define BOLD			"{!"
#define NO_COLOR		"{x" /* Default color */
#define BLINK           "{^"
#define REVERSE			"{&"
#define TILDE           "{-"




